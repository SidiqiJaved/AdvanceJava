package com.syntax.class07;

public class HW_For_Loops {
	
	public static void main(String []args) {
		
		
		
		for (int a=1; a <= 100; ++a) {
			
			System.out.print(a+ " ");
		}
		
		System.out.println(""); System.out.println("");
		
		
		for(int a=100; a >=1; --a) {
			System.out.print(a+" ");
		}
		
		System.out.println(""); System.out.println("");
		
		
		for (int a=20; a >=2; a=a-2 ) {
			
			System.out.print(a +" ");
		}
		System.out.println(""); System.out.println("");
		
		
		for (int a=2; a <=20; a=a+2) {
			System.out.print(a +" ");
		}
		System.out.println(""); System.out.println("");
		
		
		for (int a=21; a <=49; a =a +2) {
			
			System.out.print(a+ " ");
		}
		
		
	}

}

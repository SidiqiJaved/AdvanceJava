package com.syntax.class07;

public class DoLoopDemo {

	public static void main(String[] args) {
		
		
		
		
		
		int num=1;
		while (num<3) {
			System.out.println("Hello "); //helo five times
			num++;
			
		}
		
		int num1=1;
		
		do {
			System.out.println("Hello");
		num1++;
		}while (num1<=3);
		}
	}



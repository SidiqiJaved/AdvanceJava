package com.syntax.class01;

public class PrimitiveDataTypes {

	public static void main(String[] args) {
		
		
		// 8 data types in java for primitive type of data

		byte box1=1;
		
		short box2=2;
		
		int box3= 3321310;
		
		long box4= 9;
		
		System.out.println(box2);
		
		// for decimals numbers 
		
		float price= 12.99f;
		double price1=13.99;
		
		// to represent a single character   
		
		char gender='m';
		
		char sign='*';
		
		// to represent yes or no we use boolean 
		
		boolean snow=false;
		boolean tired= true;
		
		
		
		
	}
}
 